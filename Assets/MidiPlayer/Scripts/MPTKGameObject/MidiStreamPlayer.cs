    public partial class MidiStreamPlayer : MidiSynth
    {
        /// <summary>
        /// Description and list of MIDI channels associated with the synth.
        /// Each MIDI synth has 16 channels that carry all the relevant MIDI information.
        ///     - Current instrument / bank
        ///     - Volume
        ///     - Mute / Unmute (see Enable)
        ///     - Pitch bend ...
        ///     
        /// Channels distinguish instruments and provide independent control for each one.
        /// By sending MIDI messages on each channel, you can change instrument, volume, pitch, and other parameters.
        /// In Maestro MPTK, channels are indexed from 0 to 15. Channel 9 is reserved for drum sounds.
        /// 
        /// @snippet TestMidiFilePlayerScripting.cs ExampleUsingChannelAPI_Full
        /// </summary>
        /// @ingroup midistreamplayer_channels
        public MPTKChannels MPTK_Channels
        {
            get { return Channels; }
        }

        new void Awake()
        {
            base.Awake();
        }

        new void Start()
        {
            MPTK_StartMidiStream();
        }

        // removed V2.89.0
