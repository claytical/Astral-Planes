    /// @ingroup midistreamplayer_management
    /// <summary>
    /// Builds and plays real-time music in response to user actions or algorithmic logic.
    /// This class is intended to be used with the MidiStreamPlayer prefab.
    /// 
    /// @attention 
    ///     - MidiStreamPlayer inherits from MidiSynth. For clarity, only MidiStreamPlayer-specific members are documented here.
    ///       See MidiSynth for the full list of inherited members.
    ///       
    /// No MIDI file is required. Notes are generated by your scripts with this class API.
    /// The main method, MPTK_PlayEvent(), together with MPTKEvent, allows you to create MIDI events such as note-on.
    /// Set all relevant values in MPTKEvent (command, note value, duration, and so on). See MPTKEvent for details.
    ///
    /// A note-on must eventually be stopped. For example, if duration = -1, the note is infinite
    /// and must be stopped with MPTK_StopEvent() (which sends a note-off).
    /// In Maestro Pro, chord playback is also available with MPTK_PlayChordFromRange() and MPTK_PlayChordFromLib().
    /// For scale playback, see MPTKRangeLib.
    /// For more information, see https://paxstellar.fr/midi-file-player-detailed-view-2-2/
    /// and the TestMidiStream demo source (TestMidiStream.cs).
    ///
    /// Because this class inherits from MidiSynth, all MidiSynth properties, events,
    /// and methods are also available here.
    ///
    /// @note
    ///     - A full example is provided below.
    ///     - You cannot copy and paste this extract directly, but the complete source code
    ///       is available with Maestro MPTK Free.
    ///     - Some features are available only with the Pro version.
    /// @snippet TestMidiStream.cs ExampleFullMidiStream
    /// </summary>
